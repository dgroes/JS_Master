let dia = 6;

switch (dia) {
    case 5:
        console.log("Hoy es viernes 🎉");
        break;
    case 4:
        console.log("Hoy es jueves 😙");
        break;
    case 3:
        console.log("Hoy es mircoles 😫");
        break;
    case 2:
        console.log("Hoy es martes 🤯🔫");
        break;
    case 1:
        console.log("Hoy es lunes 😠");
        break;

    default:
        console.log("Es fin de semana 🐱");
        break;
}