let a = 10;
//Operadores relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//Operadores de igualdad
console.log(a == 10); //igual que 
console.log(a != 10); //distinto de
console.log(a == '10'); // el simbolo == pregunta si son iguales, ignorando el tipo de dato
console.log(a === '10'); // con === comprueba si tiene el mismo tipo y valor
console.log(a !== '10'); // disito tipo de dato o de valor se utiliza !==